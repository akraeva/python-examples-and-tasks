import pytest
from src.module_1 import m_1_2_1


@pytest.mark.parametrize(
    "inputs, expected",
    [
        (
            ["35", "1.8", "120.71"],
            "bmi= 37.26 Вы относитесь к группе людей с ожирением.\n",
        ),
        (
            ["18", "1.68", "82.79"],
            "bmi= 29.33 Вы относитесь к группе людей с избыточной массой тела.\n",
        ),
        (
            ["50", "1.97", "89.67"],
            "bmi= 23.11 Вы относитесь к группе людей с нормальной массой тела.\n",
        ),
        # Дополнительные тесты по таблице
        (
            ["30", "1.75", "55"],
            "bmi= 17.96 Вы относитесь к группе людей с недостаточной массой тела.\n",
        ),  # <18.5
        (
            ["40", "1.65", "75"],
            "bmi= 27.55 Вы относитесь к группе людей с избыточной массой тела.\n",
        ),  # 25-29.99
        (
            ["60", "1.60", "90"],
            "bmi= 35.16 Вы относитесь к группе людей с ожирением.\n",
        ),  # >=32
    ],
)
def test_1_2_1(inputs, expected, mocker, capsys):
    """
    Тесты для вычисления ИМТ с проверкой границ по возрасту.
    """
    mocker.patch("builtins.input", side_effect=inputs)
    m_1_2_1()
    captured = capsys.readouterr()
    assert captured.out == expected
